// Declarar variable tipo string
const nombre = 'Juan';
console.log(nombre); // Imprimir variable por consola

// Declarar variable tipo number
const edad = 18;
console.log(edad); // Imprimir variable por consola

// Declarar variable tipo boolean
const puedoConducir = true;
console.log(puedoConducir); // Imprimir variable por consola

// Imprimir el tipo de dato de cada variable
console.log(typeof nombre);
console.log(typeof edad);
console.log(typeof puedoConducir);